// Demonstrate the relational operators
class RelOps {
  public static void main(String args[]) {
    int one = 1, ten = 10;
    System.out.println(one + " < " + ten + " : " + (one < ten));
    System.out.println(one + " <= " + ten + " : " + (one <= ten));
    System.out.println(one + " == " + ten + " : " + (one == ten));
    System.out.println(one + " != " + ten + " : " + (one != ten));
    System.out.println(one + " > " + ten + " : " + (one > ten));
    System.out.println(one + " >= " + ten + " : " + (one >= ten));
  }
}